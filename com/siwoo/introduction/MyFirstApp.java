package com.siwoo.introduction;

public /* 접근 제한자*/ class MyFirstApp /* 클래스 이름 */ {  /* scope */
    
    public /* 접근 제한자 */ static  /* 다음에 설명 */
        void /* main 함수의 리턴 타입 */ main /* 함수의 이름 */ 
        (String[] args) /* main 함수의 파라미터 (parameter) */ {   /* inner scope */
        System.out.println("해석해보자" /* println 의 인자 (argument) */);  
    } /* inner scope end */
    
} /* end scope */
